-- 删除表（如果存在）
DROP TABLE IF EXISTS crop;

-- 创建表
CREATE TABLE crop (
    id INT AUTO_INCREMENT PRIMARY KEY COMMENT '自增主键',
    crop_id VARCHAR(32) COMMENT '作物ID',
    crop_name VARCHAR(255) COMMENT '作物名称',
    crop_variety VARCHAR(255) COMMENT '作物品种',
    variety_source_company VARCHAR(255) COMMENT '品种来源公司',
    variety_production_time DATE COMMENT '品种生产时间',
    family VARCHAR(255) COMMENT '科属',
    image_url VARCHAR(255) COMMENT '图片URL',
    description TEXT COMMENT '描述',
    crop_history TEXT COMMENT '作物历史',
    remarks TEXT COMMENT '备注',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间，默认为当前时间',
    updated_by VARCHAR(50) DEFAULT 'system' COMMENT '更新人，默认为system',
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '最后更新时间，默认为当前时间，并在每次更新时自动更新',
    created_by VARCHAR(50) DEFAULT 'system' COMMENT '创建人，默认为system'
) COMMENT='作物信息表';



